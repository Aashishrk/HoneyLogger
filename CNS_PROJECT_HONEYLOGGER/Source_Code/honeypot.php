<p>
The honeypot works by, hiding the input from human visitors, but not the spambots.
The spambots fill it in just like any other input which tells the PHP or JS Validation that it's spam and prevents the email from sending.
So we would call the input something unsuspecting so the bot fills it in and does not become wise to our ways.
Placeholder is in place so that if CSS is off the human user knows not to fill it in (so again may want to say something more general here).
And autocomplete off is in place so that the human users browser does not by mistake fill in the hidden input.</p>
